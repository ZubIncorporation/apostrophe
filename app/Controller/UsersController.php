<?php
App::uses('AppController', 'Controller');
/**
 * Users Controller
 *
 * @property User $User
 */
class UsersController extends AppController {

	public $actionsList = array(
		'all' => array(
			'Gestion des Droits'=> array('controller' => 'acl', 'action' => 'index', 'admin'=>true),
			'Groupes' => array('controller' => 'groups', 'action' => 'index', 'admin'=>true)
		),
		'index' => array(
			'Ajouter Membre' => array('controller' => 'users', 'action' => 'add', 'admin'=>false)
		)
	);

	/*
		BeforeFilter : 
		Pour Ã©viter le bug requestAction avec AuthComponent
		isLogged : pour la sidebar, et Ã©ventuellement d'autres vues...
	*/
	
	public function beforeFilter() {
	    parent::beforeFilter();
	    $this->Auth->allow('isLogged');
	}
	
	public function isLogged(){ 
		if(empty($this->request->params['requested'])){ 
			throw new ForbiddenException();
		}
		
		if($this->Auth->loggedIn() && $this->Auth->user('id') != $this->User->guestID)
			return true;
		else
			return false;
	}
	
	
/**
 * 
 * ACL Directives
 * http://book.cakephp.org/2.0/en/tutorials-and-examples/simple-acl-controlled-application/simple-acl-controlled-application.html
 * 
 */

	public function login() {		
	    if ($this->request->is('post')) {
	    	$this->Auth->logout();
	        if ($this->Auth->login()) {
	            $this->redirect($this->Auth->redirect());
	        } else {
	            $this->Session->setFlash('Your username or password was incorrect.');
	        }
	    }
	}
	
	public function logout() {
	   $this->redirect($this->Auth->logout());
	}


/**
 * 
 * BAKE GENERATION 
 * 
 */
 
 
 
 
/**
 * index method
 *
 * @return void
 */
	public function index() {
		$this->User->recursive = 0;
		$this->set('users', $this->paginate());
	}

/**
 * view method
 *
 * @throws NotFoundException
 * @param string $id
 * @return void
 */
	public function view($id = null) {
		$this->User->id = $id;
		if (!$this->User->exists()) {
			throw new NotFoundException(__('Invalid user'));
		}
		$this->set('user', $this->User->read(null, $id));
	}

/**
 * add method
 *
 * @return void
 */
	public function add() {
		if ($this->request->is('post')) {
			$this->User->create();
			if ($this->User->save($this->request->data)) {
				$this->Session->setFlash(__('The user has been saved'));
				$this->redirect(array('action' => 'index'));
			} else {
				$this->Session->setFlash(__('The user could not be saved. Please, try again.'));
			}
		}
		$groups = $this->User->Group->find('list');
		$this->set(compact('groups'));
	}

/**
 * edit method
 *
 * @throws NotFoundException
 * @param string $id
 * @return void
 */
	public function edit($id = null) {
		$this->User->id = $id;
		if (!$this->User->exists()) {
			throw new NotFoundException(__('Invalid user'));
		}
		if ($this->request->is('post') || $this->request->is('put')) {
			if ($this->User->save($this->request->data)) {
				$this->Session->setFlash(__('The user has been saved'));
				$this->redirect(array('action' => 'index'));
			} else {
				$this->Session->setFlash(__('The user could not be saved. Please, try again.'));
			}
		} else {
			$this->request->data = $this->User->read(null, $id);
		}
		$groups = $this->User->Group->find('list');
		$this->set(compact('groups'));
	}

/**
 * delete method
 *
 * @throws MethodNotAllowedException
 * @throws NotFoundException
 * @param string $id
 * @return void
 */
	public function delete($id = null) {
		if (!$this->request->is('post')) {
			throw new MethodNotAllowedException();
		}
		$this->User->id = $id;
		if (!$this->User->exists()) {
			throw new NotFoundException(__('Invalid user'));
		}
		if ($this->User->delete()) {
			$this->Session->setFlash(__('User deleted'));
			$this->redirect(array('action' => 'index'));
		}
		$this->Session->setFlash(__('User was not deleted'));
		$this->redirect(array('action' => 'index'));
	}

